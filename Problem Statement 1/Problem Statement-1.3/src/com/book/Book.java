package com.book;

public class Book {
	
	private String BookTitle;
	private float BookPrice;
	
	public String getBookTitle() {
		return BookTitle;
	}
	public void setBookTitle(String bookTitle) {
		BookTitle = bookTitle;
	}
	public float getBookPrice() {
		return BookPrice;
	}
	public void setBookPrice(float bookPrice) {
		BookPrice = bookPrice;
	}

}
